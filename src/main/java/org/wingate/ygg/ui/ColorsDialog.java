    private void btnAssBGRPasteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssBGRPasteActionPerformed
        String paste = Clipboard.CPaste();
        paste = paste.replace("&", "");
        paste = paste.replace("H", "");
        if(paste.matches("[A-Fa-f0-9]{6}")){
            tfAssBGR.setText(paste.toUpperCase());
            jColorChooser1.setColor(getColorFromHexa(paste));
        }
    }//GEN-LAST:event_btnAssBGRPasteActionPerformed

