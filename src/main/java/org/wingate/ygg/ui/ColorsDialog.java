    private int getInteger(String hexa){
        return Integer.parseInt(hexa, 16);
    }
    
    private Color getColorFromHexa(String h){
        return new Color(
                getInteger(h.substring(4)),         // R
                getInteger(h.substring(2, 4)),      // G
                getInteger(h.substring(0, 2))       // B
        );
    }
    
    private String getBGR(Color c){
        String B = Integer.toHexString(c.getBlue());        
        String G = Integer.toHexString(c.getGreen());
        String R = Integer.toHexString(c.getRed());
        
        if(B.length() == 1) B = "0" + B;
        if(G.length() == 1) G = "0" + G;
        if(R.length() == 1) R = "0" + R;
        
        return (B+G+R).toUpperCase();
    }

