    private void mnuBeforeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuBeforeActionPerformed
        if(MainFrame.getTableFrame() == null) return;
        if(MainFrame.getTableFrame().getFormat() == SelectedFormat.ASS){
            //===---
            IfrTable table = MainFrame.getTableFrame();
            //===---
            AssEvent nv = assEvent;
            nv.setStartTime(Time.create(tfStart.getText()));
            nv.setEndTime(Time.create(tfEnd.getText()));
            nv.setText(tpPR.getText());
            if(table.getTable().getSelectedRow() != -1){
                table.getLastAssSynchroTable().getAssTableModel().insertOneAt(nv, table.getTable().getSelectedRow());
                table.getLastAssSynchroTable().getTable().updateUI();
            }
        }
    }//GEN-LAST:event_mnuBeforeActionPerformed

    private void mnuAfterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuAfterActionPerformed
        if(MainFrame.getTableFrame() == null) return;
        if(MainFrame.getTableFrame().getFormat() == SelectedFormat.ASS){
            //===---
            IfrTable table = MainFrame.getTableFrame();
            //===---
            AssEvent nv = assEvent;
            nv.setStartTime(Time.create(tfStart.getText()));
            nv.setEndTime(Time.create(tfEnd.getText()));
            nv.setText(tpPR.getText());
            if(table.getTable().getSelectedRow() != -1){
                if(table.getTable().getRowCount() - 1 == table.getTable().getSelectedRow()){
                    // We are at last event
                    table.getLastAssSynchroTable().getAssTableModel().insertOne(nv);
                }else{
                    // We are inside the events cosmos
                    table.getLastAssSynchroTable().getAssTableModel().insertOneAt(nv, table.getTable().getSelectedRow() + 1);
                }
                table.getLastAssSynchroTable().getTable().updateUI();
            }
        }
    }//GEN-LAST:event_mnuAfterActionPerformed

    private void mnuSendProposalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuSendProposalActionPerformed
        PRDialog pr = new PRDialog(new JFrame(), true);
        pr.showDialog(MainFrame.getCryptObjs());

        List<InSubsMessage> list = new ArrayList<>();
        AssEvent nv = assEvent;
        nv.setLineType(AssEvent.LineType.Proposal);
        nv.setStartTime(Time.create(tfStart.getText()));
        nv.setEndTime(Time.create(tfEnd.getText()));
        nv.setText(tpPR.getText());
        list.add(new InSubsMessage(InSubsMessage.InSubsType.Proposal, AssEvent.getAssEventLine(nv)));

        if(list.isEmpty() == false && pr.getDialogResult() == PRDialog.DialogResult.OK){
            for(YggLock.CryptObj co : pr.getSelection()){
                Client.sendProposalMessage(co, list);
            }            
        }
    }//GEN-LAST:event_mnuSendProposalActionPerformed

