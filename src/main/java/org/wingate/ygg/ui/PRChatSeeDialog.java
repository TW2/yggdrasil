    public void showDialog(Object obj, Type type){
        if(obj instanceof AssEvent){
            AssEvent ev = (AssEvent)obj;
            assEvent = ev;
            tfStart.setText(ev.getStartTime().toASSTime());
            tfEnd.setText(ev.getEndTime().toASSTime());
            tpPR.setText(ev.getText());
            switch(type){
                case Proposal -> { lblPR.setText("Proposal"); }
                case Request -> { lblPR.setText("Request"); }
            }
            setVisible(true);
        }
    }

