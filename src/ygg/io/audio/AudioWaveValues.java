package ygg.io.audio;

/**
 *
 * @author util2
 */
public class AudioWaveValues {
    private double x = 0d;
    private double y = 0d;

    public AudioWaveValues() {
    }

    public static AudioWaveValues create(double x, double y){
        AudioWaveValues wv = new AudioWaveValues();

        wv.x = x;
        wv.y = y;

        return wv;
    }


    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
